package operators;

public class rational {
	public void eqal(int a,int b)
	{
		System.out.println("a=b"+(a==b));
	}
	public void NOTeqal(int a,int b)
	{
		System.out.println("a!=b"+(a!=b));
	}

	public void LesserEqal(int a,int b)
	{
		System.out.println("a<=b"+(a<=b));
	}
	public void GreaterEqal(int a,int b)
	{
		System.out.println("a>=b"+(a==b));
	}

	public void Greater(int a,int b)
	{
		System.out.println("a>b"+(a>b));
	}
	public void Lesser(int a,int b)
	{
		System.out.println("a<b"+(a==b));
	}


}
